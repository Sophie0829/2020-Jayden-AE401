import turtle
S=turtle.Turtle()
F=turtle.Screen()

F.title("window")

F.bgcolor("black")

S.color("green")
S.shape("turtle")
S.pensize(10)

A=0

while A<36:
    S.left(10)
    S.forward(10)
    A=A+1

turtle.done()












